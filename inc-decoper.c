int main (){
	int a;
	printf("Enter the Value of a:");
	scanf("%d",&a);
	printf("Original value of a=%d\n",a);
	printf("The post-fix increment(a++) is %d\n",a++);
	printf("The pre-fix increment(++a) is %d\n",++a);
	
	printf("Original value of a=%d\n",a);
	printf("The post-fix decrement(a--) is %d\n",a--);
	printf("The pre-fix decrement(--a) is %d\n",--a);
	//getch();
	return 0;
}
	
	
