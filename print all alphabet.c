int main()
{
	char i='A',j='a';
	printf("Upper case alphabets are:\n");
	while(i<='Z')
	{
		printf("%c\t",i);
		i++;
	}
	printf("\n\n");
	printf("Lower case alphabets are:\n");
	while(j<='z')
	{
		printf("%c\t",j);
		j++;
	}
	return 0;
}
